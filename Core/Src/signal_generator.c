void scaleSineWave(void)
{
	for(unsigned i = 0; i < numSamples; i++)
	{
		sineWaveScaledLut[i] = sineWaveLut[i] / 10;
	}
}

static unsigned frequencyToArr(TIM_HandleTypeDef* htim, unsigned frequency, unsigned numberOfSamples)
{
	unsigned clkFreq = HAL_RCC_GetPCLK1Freq();
	unsigned psc = htim->Instance->PSC;
	unsigned arr = (2 * clkFreq) / (frequency * (psc + 1) * numberOfSamples);
	if(arr > 0xFFFF)
		arr = 0xFFFF;

	return arr;
}

static void startOscillator(void)
{
	HAL_TIM_Base_Start(generator.htim);
}

void signalGeneratorPlayNote(MusicNote note)
{
	unsigned arr = frequencyToArr(generator.htim, (unsigned)note, generator.numberOfSamples);

	 __HAL_TIM_SET_AUTORELOAD(generator.htim, arr);
	 startOscillator();
}

void signalGeneratorInit(TIM_HandleTypeDef* htim)
{
	generator.htim = htim;
}

void signalGeneratorStop(void)
{
	HAL_TIM_Base_Stop(generator.htim);
}

